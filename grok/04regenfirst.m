**Hard Rule**  
No module may begin development, procurement, or hiring until **every** listed trigger for that priority is verifiably satisfied.  
Failure of any trigger → delay or permanent skip of that module.

**Mandatory Review Gates**  
- **Dec 31, 2029**: Confirm network self-funding → decide on Industrial Gases  
- **Dec 31, 2030**: Evaluate Mini-Fab & DAC readiness  
- **Dec 31, 2032**: Final decision gate for consumer electronics & cultured meat  

